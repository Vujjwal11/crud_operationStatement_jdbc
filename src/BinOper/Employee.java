package BinOper;

public class Employee {
	private int eid;
	private String ename;
	private String esub;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getEsub() {
		return esub;
	}
	public void setEsub(String esub) {
		this.esub = esub;
	}
	
}
